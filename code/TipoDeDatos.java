package com;

public class TipoDeDatos {

	public static void main(String[] args) {
		int numero = 100;
		float numero_decimal = 1.2F;
		double numero_decimal2 = 1.7;
		boolean logico=false;
		String palabra = "Tomate";
		char letra= 'd';
		
		
		System.out.println("Numero: " +numero);
		System.out.println("numero_decimal: " +numero_decimal);
		System.out.println("numero_decimal2: " +numero_decimal2);
		System.out.println("logico: " +logico);
		System.out.println("palabra: " +palabra);
		System.out.println("letra: " +letra);
			
	}

}
